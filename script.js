function getThumbnailUrl(url) {

    if (!url) return '';

    // Convert Dropbox preview links to direct image links

    if (url.includes('dropbox.com') && !url.includes('dl=1')) {

        url = url.replace('dl=0', 'dl=1');

        if (!url.includes('dl=1')) {

            url += (url.includes('?') ? '&' : '?') + 'dl=1';

        }

    }

    return url;

}

